    CASE (ERRFLAG_UNABLE_TO_PRINT_FILTER)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to print the filter' )
    CASE (ERRFLAG_UNABLE_TO_PRINT_ASSIGNMENT)
