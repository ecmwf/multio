  WRITE( UNIT, * ) ' '

  !> Print the filter
  PP_TRYCALL(ERRFLAG_UNABLE_TO_PRINT_FILTER) THIS%FILTER_%PRINT( UNIT, OFFSET+2, HOOKS )
  WRITE( UNIT, * ) ' '

  !> Print the assignment
  PP_TRYCALL(ERRFLAG_UNABLE_TO_PRINT_ASSIGNMENT) THIS%ASSIGNMENT_%PRINT( UNIT, OFFSET+2, HOOKS )

