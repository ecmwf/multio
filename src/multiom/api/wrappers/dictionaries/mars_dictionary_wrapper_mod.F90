  ! Function result
  INTEGER(KIND=JPIB_K) :: RET

  ! Local variables
  TYPE(FORTRAN_MESSAGE_T), POINTER :: DICTIONARY

  ! Local error flags
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_WRAPPER_ALREADY_ASSOCIATED=1_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UNABLE_TO_MARS_DICTIONARY_WRAPPER_CREATE=2_JPIB_K
