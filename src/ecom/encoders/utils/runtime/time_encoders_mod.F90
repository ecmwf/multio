SUBROUTINE SET_END_OF_TIME_INTERVAL( CURR_TIME, CURRENT_TIME_IN_SEC, METADATA )

  ! Symbols imported from other modules within the project.
  USE :: OM_CORE_MOD,        ONLY: JPIB_K
  USE :: OM_CORE_MOD,        ONLY: JPIM_K
  USE :: OM_CORE_MOD,        ONLY: CURR_TIME_T
  USE :: OM_CORE_MOD,        ONLY: OM_ATM_MSG_T
  USE :: METADATA_BASE_MOD,  ONLY: METADATA_BASE_A

  ! Symbols imported from external libraries
  USE :: ECCODES, ONLY: CODES_JULIAN_TO_DATETIME
  USE :: ECCODES, ONLY: CODES_DATETIME_TO_JULIAN
  USE :: ECCODES, ONLY: CODES_SUCCESS
  USE :: ECCODES, ONLY: CODES_GET_ERROR_STRING

  ! Symbols imported by the preprocessor for debugging purposes
  PP_DEBUG_USE_VARS

  ! Symbols imported by the preprocessor for tracing purposes
  PP_TRACE_USE_VARS

IMPLICIT NONE

  ! Dummy arguments
  TYPE(CURR_TIME_T),               INTENT(IN)    :: CURR_TIME
  INTEGER(KIND=JPIB_K),            INTENT(IN)    :: CURRENT_TIME_IN_SEC
  CLASS(METADATA_BASE_A), POINTER, INTENT(INOUT) :: METADATA

  ! Local variables
  INTEGER(KIND=JPIB_K), DIMENSION(6) :: DATE_TIME
  REAL(KIND=C_DOUBLE) :: JDT
  INTEGER(KIND=JPIM_K) :: KRET

  ! Local variables declared by the preprocessor for debugging purposes
  PP_DEBUG_DECL_VARS

  ! Local variables declared by the preprocessor for tracing purposes
  PP_TRACE_DECL_VARS

  ! Trace begin of procedure
  PP_TRACE_ENTER_PROCEDURE()
  PP_METADATA_ENTER_PROCEDURE( METADATA )

  DATE_TIME = -1_JPIB_K

  ! Convert date time to julian date
  CALL CODES_DATETIME_TO_JULIAN( CURR_TIME%SIM_REF_TIME(1), CURR_TIME%SIM_REF_TIME(2), CURR_TIME%SIM_REF_TIME(3), CURR_TIME%SIM_REF_TIME(4), CURR_TIME%SIM_REF_TIME(5), CURR_TIME%SIM_REF_TIME(6), JDT, STATUS=KRET )
  PP_DEBUG_CRITICAL_COND_THROW( KRET.NE.CODES_SUCCESS, 1 )

  ! Add forecast time to julian date
  JDT = JDT + REAL(CURRENT_TIME_IN_SEC,KIND=C_DOUBLE) * SEC_TO_DAYS

  ! Convert julian date to date time
  CALL CODES_JULIAN_TO_DATETIME( JDT, DATE_TIME(1), DATE_TIME(2), DATE_TIME(3), DATE_TIME(4), DATE_TIME(5), DATE_TIME(6), STATUS=KRET )
  PP_DEBUG_CRITICAL_COND_THROW( KRET.NE.CODES_SUCCESS, 2 )

  PP_METADATA_SET( METADATA, 'yearOfEndOfOverallTimeInterval',   DATE_TIME(1))
  PP_METADATA_SET( METADATA, 'monthOfEndOfOverallTimeInterval',  DATE_TIME(2))
  PP_METADATA_SET( METADATA, 'dayOfEndOfOverallTimeInterval',    DATE_TIME(3))
  PP_METADATA_SET( METADATA, 'hourOfEndOfOverallTimeInterval',   DATE_TIME(4))
  PP_METADATA_SET( METADATA, 'minuteOfEndOfOverallTimeInterval', DATE_TIME(5))
  PP_METADATA_SET( METADATA, 'secondOfEndOfOverallTimeInterval', DATE_TIME(6))

  ! Trace end of procedure (on error)
  PP_METADATA_EXIT_PROCEDURE( METADATA )
  PP_TRACE_EXIT_PROCEDURE_ON_SUCCESS()

  ! Exit point
  RETURN


! Error handler
PP_ERROR_HANDLER

  ErrorHandler: BLOCK

    ! Error handling variables
    CHARACTER(LEN=:), ALLOCATABLE :: STR
    CHARACTER(LEN=4096) :: CODES_MSG
    CHARACTER(LEN=4096) :: CIDT

    ! HAndle different errors
    SELECT CASE(ERRIDX)
    CASE (1)
      CODES_MSG = REPEAT(' ', 4096)
      CIDT = REPEAT(' ',4096)
      WRITE(CIDT,'(6I5)') CURR_TIME%SIM_REF_TIME
      CALL CODES_GET_ERROR_STRING( KRET, CODES_MSG )
      PP_DEBUG_CREATE_ERROR_MSG_GRIB( STR, 'Unable to convert date to Julian date: '//TRIM(ADJUSTL(CIDT)), KRET, CODES_MSG )
    CASE (2)
      CODES_MSG = REPEAT(' ', 4096)
      CIDT = REPEAT(' ',4096)
      WRITE(CIDT,'(6I5)') CURR_TIME%SIM_REF_TIME
      CALL CODES_GET_ERROR_STRING( KRET, CODES_MSG )
      PP_DEBUG_CREATE_ERROR_MSG_GRIB( STR, 'Unable to convert Julian Date to date: '//TRIM(ADJUSTL(CIDT)), KRET, CODES_MSG )
    CASE DEFAULT
      PP_DEBUG_CREATE_ERROR_MSG( STR, 'Unhandled error' )
    END SELECT

    ! Trace end of procedure (on error)
    PP_TRACE_EXIT_PROCEDURE_ON_ERROR()

    ! Write the error message and stop the program
    PP_DEBUG_ABORT( STR )

  END BLOCK ErrorHandler

  ! Exit point on error
