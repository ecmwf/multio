        F_ITERATOR(1) = 0_C_INT
        PP_TRYCALL(ERRFLAG_DEALLOC_STR) DEALLOCATE_ITERATOR( F_ITERATOR, HOOKS )
        C_IT_P = C_NULL_PTR
        C_KEY_P   = C_NULL_PTR
        C_VAL_P = C_NULL_PTR

      ELSE

        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_KEY) REGULAR_GG_DICTIONARY_GET_KEY_AS_STRING( REGULAR_GG_DICT, ITERATOR, F_KEY, HOOKS )
        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_VAL) REGULAR_GG_DICTIONARY_GET_VALUE_AS_STRING( REGULAR_GG_DICT, ITERATOR, C_VAL_P, L_HAS, HOOKS )
        PP_TRYCALL(ERRFLAG_CONVERT_TO_C_STRING) CONVERT_TO_C_STRING( F_KEY, C_KEY_P, HOOKS )

      ENDIF

      REGULAR_GG_DICT => NULL()
    END BLOCK

  CASE ( REGULAR_LL_DICTIONARY_IDX_E )

    BLOCK
      TYPE(REGULAR_LL_T), POINTER :: REGULAR_LL_DICT
      REGULAR_LL_DICT => NULL()

      ! Extract dictionary
      PP_TRYCALL(ERRFLAG_EXTRACT_DICTIONARY) EXTRACT_REGULAR_LL_DICTIONARY( C_DICT_P, REGULAR_LL_DICT, HOOKS )

      ! Go to the next iterator or deallocate every output
      IF ( .NOT.C_ASSOCIATED(C_IT_P) ) THEN
        F_ITERATOR => NULL()
        PP_TRYCALL(ERRFLAG_ALLOC_FAILURE) ALLOCATE_ITERATOR( F_ITERATOR, HOOKS )
        ITERATOR = F_ITERATOR(1)
        PP_TRYCALL(ERRFLAG_INITIALIZE_ITERATOR) REGULAR_LL_DICTIONARY_INIT_ITERATOR( REGULAR_LL_DICT, ITERATOR, HOOKS )
        F_ITERATOR(1) = INT(ITERATOR,KIND=C_INT)
        C_IT_P = C_LOC(F_ITERATOR)
        END_OF_ITERATORS = .FALSE.
      ELSE
        CALL C_F_POINTER( C_IT_P, F_ITERATOR, [1] )
        ITERATOR = INT( F_ITERATOR(1), KIND=C_INT )
        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_NEXT_IT) REGULAR_LL_DICTIONARY_GET_NEXT_ITERATOR( REGULAR_LL_DICT, ITERATOR, END_OF_ITERATORS, HOOKS )
        F_ITERATOR(1) = INT(ITERATOR,KIND=C_INT)
      ENDIF


      ! If the iterator is out of bounds, then deallocate it
      IF ( END_OF_ITERATORS ) THEN
        F_ITERATOR(1) = 0_C_INT
        PP_TRYCALL(ERRFLAG_DEALLOC_STR) DEALLOCATE_ITERATOR( F_ITERATOR, HOOKS )
        C_IT_P = C_NULL_PTR
        C_KEY_P   = C_NULL_PTR
        C_VAL_P = C_NULL_PTR

      ELSE

        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_KEY) REGULAR_LL_DICTIONARY_GET_KEY_AS_STRING( REGULAR_LL_DICT, ITERATOR, F_KEY, HOOKS )
        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_VAL) REGULAR_LL_DICTIONARY_GET_VALUE_AS_STRING( REGULAR_LL_DICT, ITERATOR, C_VAL_P, L_HAS, HOOKS )
        PP_TRYCALL(ERRFLAG_CONVERT_TO_C_STRING) CONVERT_TO_C_STRING( F_KEY, C_KEY_P, HOOKS )

      ENDIF

      REGULAR_LL_DICT => NULL()
    END BLOCK


  CASE ( SH_DICTIONARY_IDX_E )

    BLOCK
      TYPE(SH_T), POINTER :: SH_DICT
      SH_DICT => NULL()

      ! Extract dictionary
      PP_TRYCALL(ERRFLAG_EXTRACT_DICTIONARY) EXTRACT_SH_DICTIONARY( C_DICT_P, SH_DICT, HOOKS )

      ! Go to the next iterator or deallocate every output
      IF ( .NOT.C_ASSOCIATED(C_IT_P) ) THEN
        F_ITERATOR => NULL()
        PP_TRYCALL(ERRFLAG_ALLOC_FAILURE) ALLOCATE_ITERATOR( F_ITERATOR, HOOKS )
        ITERATOR = F_ITERATOR(1)
        PP_TRYCALL(ERRFLAG_INITIALIZE_ITERATOR) SH_DICTIONARY_INIT_ITERATOR( SH_DICT, ITERATOR, HOOKS )
        F_ITERATOR(1) = INT(ITERATOR,KIND=C_INT)
        C_IT_P = C_LOC(F_ITERATOR)
        END_OF_ITERATORS = .FALSE.
      ELSE
        CALL C_F_POINTER( C_IT_P, F_ITERATOR, [1] )
        ITERATOR = INT( F_ITERATOR(1), KIND=C_INT )
        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_NEXT_IT) SH_DICTIONARY_GET_NEXT_ITERATOR( SH_DICT, ITERATOR, END_OF_ITERATORS, HOOKS )
        F_ITERATOR(1) = INT(ITERATOR,KIND=C_INT)
      ENDIF


      ! If the iterator is out of bounds, then deallocate it
      IF ( END_OF_ITERATORS ) THEN
        F_ITERATOR(1) = 0_C_INT
        PP_TRYCALL(ERRFLAG_DEALLOC_STR) DEALLOCATE_ITERATOR( F_ITERATOR, HOOKS )
        C_IT_P = C_NULL_PTR
        C_KEY_P   = C_NULL_PTR
        C_VAL_P = C_NULL_PTR

      ELSE

        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_KEY) SH_DICTIONARY_GET_KEY_AS_STRING( SH_DICT, ITERATOR, F_KEY, HOOKS )
        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_VAL) SH_DICTIONARY_GET_VALUE_AS_STRING( SH_DICT, ITERATOR, C_VAL_P, L_HAS, HOOKS )
        PP_TRYCALL(ERRFLAG_CONVERT_TO_C_STRING) CONVERT_TO_C_STRING( F_KEY, C_KEY_P, HOOKS )

      ENDIF

      SH_DICT => NULL()
    END BLOCK

  CASE ( OPT_DICTIONARY_IDX_E )

    BLOCK

      TYPE(API_OPTIONS_T), POINTER :: OPT_DICT
      OPT_DICT => NULL()

      ! Extract dictionary
      PP_TRYCALL(ERRFLAG_EXTRACT_DICTIONARY) EXTRACT_OPT_DICTIONARY( C_DICT_P, OPT_DICT, HOOKS )

      ! Go to the next iterator or deallocate every output
      IF ( .NOT.C_ASSOCIATED(C_IT_P) ) THEN
        F_ITERATOR => NULL()
        PP_TRYCALL(ERRFLAG_ALLOC_FAILURE) ALLOCATE_ITERATOR( F_ITERATOR, HOOKS )
        ITERATOR = F_ITERATOR(1)
        PP_TRYCALL(ERRFLAG_INITIALIZE_ITERATOR) OPTIONS_DICTIONARY_INIT_ITERATOR( OPT_DICT, ITERATOR, HOOKS )
        F_ITERATOR(1) = INT(ITERATOR,KIND=C_INT)
        C_IT_P = C_LOC(F_ITERATOR)
        END_OF_ITERATORS = .FALSE.
      ELSE
        CALL C_F_POINTER( C_IT_P, F_ITERATOR, [1] )
        ITERATOR = INT( F_ITERATOR(1), KIND=C_INT )
        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_NEXT_IT) OPTIONS_DICTIONARY_GET_NEXT_ITERATOR( OPT_DICT, ITERATOR, END_OF_ITERATORS, HOOKS )
        F_ITERATOR(1) = INT(ITERATOR,KIND=C_INT)
      ENDIF

      ! If the iterator is out of bounds, then deallocate it
      IF ( END_OF_ITERATORS ) THEN
        F_ITERATOR(1) = 0_C_INT
        PP_TRYCALL(ERRFLAG_DEALLOC_STR) DEALLOCATE_ITERATOR( F_ITERATOR, HOOKS )
        C_IT_P = C_NULL_PTR
        C_KEY_P   = C_NULL_PTR
        C_VAL_P = C_NULL_PTR

      ELSE

        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_KEY) OPTIONS_DICTIONARY_GET_KEY_AS_STRING( OPT_DICT, ITERATOR, F_KEY, HOOKS )
        PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_VAL) OPTIONS_DICTIONARY_GET_VALUE_AS_STRING( OPT_DICT, ITERATOR, C_VAL_P, L_HAS, HOOKS )
        PP_TRYCALL(ERRFLAG_CONVERT_TO_C_STRING) CONVERT_TO_C_STRING( F_KEY, C_KEY_P, HOOKS )

      ENDIF

      OPT_DICT => NULL()
    END BLOCK

  CASE DEFAULT

    ! Unknown dictionary type
    PP_DEBUG_CRITICAL_THROW( ERRFLAG_UNKNOWN_DICTIONARY_TYPE )

  END SELECT

  !> Be sure we don't have any memory leaks
  CALL HOOKS%DEBUG_HOOK_%FREE( )


  ! Trace end of procedure (on success)
  PP_TRACE_EXIT_PROCEDURE_ON_SUCCESS()

  ! Exit point (On success)
  RETURN

! Error handler
PP_ERROR_HANDLER

  ! Initialization of bad path return value
  PP_SET_ERR_FAILURE( RET )

#if defined( PP_DEBUG_ENABLE_ERROR_HANDLING )
!$omp critical(ERROR_HANDLER)

  BLOCK

    ! Error handling variables
    PP_DEBUG_PUSH_FRAME()

    SELECT CASE(ERRIDX)
    CASE (ERRFLAG_DICTIONARY_NOT_ASSOCIATED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'The provided dictionary pointer is not associated.' )
    CASE (ERRFLAG_UNABLE_TO_GET_INFO)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to get dictionary info.' )
    CASE (ERRFLAG_EXTRACT_DICTIONARY)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to extract the dictionary.' )
    CASE (ERRFLAG_UNABLE_TO_GET_MAX_ITERATOR)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to get the maximum iterator value.' )
    CASE (ERRFLAG_ALLOC_FAILURE)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to allocate memory for the iterator.' )
    CASE (ERRFLAG_INITIALIZE_ITERATOR)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to initialize the iterator.' )
    CASE (ERRFLAG_UNABLE_TO_GET_NEXT_IT)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to get the next iterator value.' )
    CASE (ERRFLAG_DEALLOC_STR)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to deallocate the iterator.' )
    CASE (ERRFLAG_UNABLE_TO_GET_KEY)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to get the key as string from the dictionary.' )
    CASE (ERRFLAG_UNABLE_TO_GET_VAL)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to get the value as string from the dictionary.' )
    CASE (ERRFLAG_CONVERT_TO_C_STRING)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to convert Fortran string to C string.' )
    CASE (ERRFLAG_UNKNOWN_DICTIONARY_TYPE)
