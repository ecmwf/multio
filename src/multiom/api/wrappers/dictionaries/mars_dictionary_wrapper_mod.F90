  PP_DEBUG_CRITICAL_COND_THROW( .NOT.ASSOCIATED(DICTIONARY), ERRFLAG_MARS_DICTIONARY_NOT_ALLOCATED)

  ! Initialize the mars dictionary
  PP_TRYCALL(ERRFLAG_UNABLE_TO_INITIALIZE_MARS_DICTIONARY) DICTIONARY%INIT( HOOKS )

  ! Inject checksum
  PP_TRYCALL(ERRFLAG_INJECT_CHECKSUM) INJECT_CHECKSUM_MARS_DICTIONARY( WRAPPED_MARS_DICTIONARY, HOOKS )

