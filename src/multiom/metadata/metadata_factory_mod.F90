  ! Initialize the output metadata
  NULLIFY( OUT_METADATA )

  ! According to the type of metadata create the right object
  SELECT TYPE(A=>IN_METADATA)
  CLASS IS (GRIB_METADATA_T)
    ALLOCATE( GRIB_METADATA_T::OUT_METADATA, STAT=ALLOC_STATUS, ERRMSG=ERRMSG )
    PP_DEBUG_CRITICAL_COND_THROW( ALLOC_STATUS.NE.0, ERRFLAG_UNABLE_TO_ALLOCATE )
