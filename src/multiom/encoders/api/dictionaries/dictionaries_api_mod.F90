      PP_TRYCALL(ERRFLAG_WRITE_YAML) REGULAR_LL_DICT%WRITE_TO_YAML(UNIT, 0_JPIB_K, HOOKS)
      REGULAR_LL_DICT => NULL()
    END BLOCK


  CASE ( SH_DICTIONARY_IDX_E )

    BLOCK
      TYPE(SH_T), POINTER :: SH_DICT
      SH_DICT => NULL()
      PP_TRYCALL(ERRFLAG_EXTRACT_DICTIONARY) EXTRACT_SH_DICTIONARY( C_DICT_P, SH_DICT, HOOKS )
      PP_TRYCALL(ERRFLAG_WRITE_YAML) SH_DICT%WRITE_TO_YAML(UNIT, 0_JPIB_K, HOOKS)
      SH_DICT => NULL()
    END BLOCK

  ! CASE ( OPT_DICTIONARY_IDX_E )

    ! BLOCK
    !   TYPE(API_OPTIONS_T), POINTER :: OPT_DICT
    !   OPT_DICT => NULL()
    !   PP_TRYCALL(ERRFLAG_EXTRACT_DICTIONARY) EXTRACT_OPTIONS_DICTIONARY( F_DICT, OPT_DICT, HOOKS )
    !   OPT_DICT => NULL()
    ! END BLOCK

  CASE DEFAULT

    ! Unknown dictionary type
    PP_DEBUG_CRITICAL_THROW( ERRFLAG_UNKNOWN_DICTIONARY_TYPE )

  END SELECT


  ! Close the stream
  SELECT CASE( TRIM(ADJUSTL(F_STREAM)) )
  CASE ('stderr')
  CASE ('stdout')
  CASE DEFAULT
    CLOSE(UNIT=UNIT)
  END SELECT

  !> Be sure we don't have any memory leaks
  CALL HOOKS%DEBUG_HOOK_%FREE( )

  ! Exit point (On success)
  RETURN

! Error handler
PP_ERROR_HANDLER

  ! Initialization of bad path return value
  PP_SET_ERR_FAILURE( RET )

#if defined( PP_DEBUG_ENABLE_ERROR_HANDLING )
!$omp critical(ERROR_HANDLER)

  BLOCK

    ! Error handling variables
    PP_DEBUG_PUSH_FRAME()

    SELECT CASE(ERRIDX)
    CASE (ERRFLAG_DICTIONARY_NOT_ASSOCIATED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'The provided dictionary pointer is not associated.' )
    CASE (ERRFLAG_OUTSTREAM_NOT_ASSOCIATED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'The provided output stream pointer is not associated.' )
    CASE (ERRFLAG_INVALID_OUTSTREAM_LENGTH)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'The provided output stream length is invalid.' )
    CASE (ERRFLAG_UABLE_TO_COPY_CPTR_TO_F_STRING)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to copy C pointer to Fortran string.' )
    CASE (ERRFLAG_OPEN_UNIT)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to open the output stream.' )
    CASE (ERRFLAG_UNABLE_TO_GET_INFO)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to get dictionary info.' )
    CASE (ERRFLAG_EXTRACT_DICTIONARY)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to extract the dictionary.' )
    CASE (ERRFLAG_WRITE_YAML)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to write the dictionary to YAML.' )
    CASE (ERRFLAG_UNKNOWN_DICTIONARY_TYPE)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unknown dictionary type.' )
    CASE DEFAULT
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unknown error' )
    END SELECT

    ! Print the error stack
    ! NOTE: This is important when c is calling this function. Is opens the error_unit
    WRITE(ERROR_UNIT,*) ' PRINT ERROR STACK FROM: "'//__FILE__//'":', __LINE__
    CALL HOOKS%DEBUG_HOOK_%PRINT_ERROR_STACK( ERROR_UNIT )

    ! Free the error stack
    CALL HOOKS%DEBUG_HOOK_%FREE( )

    ! Write the error message and stop the program
    PP_DEBUG_ABORT

  END BLOCK

!$omp end critical(ERROR_HANDLER)
#endif

  RETURN

END FUNCTION MULTIO_GRIB2_DICT_TO_YAML
