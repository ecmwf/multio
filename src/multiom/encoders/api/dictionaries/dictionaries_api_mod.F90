#undef PP_PROCEDURE_TYPE


#define PP_PROCEDURE_TYPE 'FUNCTION'
#define PP_PROCEDURE_NAME 'MULTIO_GRIB2_DICT_CREATE'
PP_THREAD_SAFE FUNCTION MULTIO_GRIB2_DICT_CREATE( C_DICT_PP, C_DICT_TYPE_P, C_LEN_I ) &
 BIND(C,NAME='multio_grib2_dict_create_f') RESULT(RET)

  !> Symbols imported from intrinsic modules.
  USE, INTRINSIC :: ISO_C_BINDING, ONLY: C_SIZE_T
  USE, INTRINSIC :: ISO_C_BINDING, ONLY: C_INT
  USE, INTRINSIC :: ISO_C_BINDING, ONLY: C_PTR
  USE, INTRINSIC :: ISO_C_BINDING, ONLY: C_NULL_PTR
  USE, INTRINSIC :: ISO_C_BINDING, ONLY: C_ASSOCIATED
  USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY: ERROR_UNIT

  ! Symbols imported from other modules within the project.
  USE :: DATAKINDS_DEF_MOD,     ONLY: JPIB_K
  USE :: HOOKS_MOD,             ONLY: HOOKS_T
  USE :: API_GENERAL_UTILS_MOD, ONLY: COPY_CPTR_TO_F_STRING
  USE :: API_GENERAL_UTILS_MOD, ONLY: DEREFERENCE_DOUBLE_C_POINTER


  ! Wrappers for the F2C interface
  USE :: MARS_DICTIONARY_WRAPPER_MOD,       ONLY: MAKE_MARS_DICTIONARY
  USE :: PAR_DICTIONARY_WRAPPER_MOD,        ONLY: MAKE_PAR_DICTIONARY
  USE :: HEALPIX_DICTIONARY_WRAPPER_MOD,    ONLY: MAKE_HEALPIX_DICTIONARY
  USE :: REDUCED_GG_DICTIONARY_WRAPPER_MOD, ONLY: MAKE_REDUCED_GG_DICTIONARY
  USE :: REGULAR_GG_DICTIONARY_WRAPPER_MOD, ONLY: MAKE_REGULAR_GG_DICTIONARY
  USE :: REGULAR_LL_DICTIONARY_WRAPPER_MOD, ONLY: MAKE_REGULAR_LL_DICTIONARY
  USE :: SH_DICTIONARY_WRAPPER_MOD,         ONLY: MAKE_SH_DICTIONARY
  USE :: OPT_DICTIONARY_WRAPPER_MOD    ,    ONLY: MAKE_OPT_DICTIONARY

  ! Symbols imported by the preprocessor for debugging purposes
  PP_DEBUG_USE_VARS

  ! Symbols imported by the preprocessor for logging purposes
  PP_LOG_USE_VARS

  ! Symbols imported by the preprocessor for tracing purposes
  PP_TRACE_USE_VARS

IMPLICIT NONE

  !> Dummy arguments
  TYPE(C_PTR),                INTENT(INOUT) :: C_DICT_PP
  TYPE(C_PTR), VALUE,         INTENT(IN)    :: C_DICT_TYPE_P
  INTEGER(KIND=C_INT), VALUE, INTENT(IN)    :: C_LEN_I

  !> Function result
  INTEGER(KIND=C_INT) :: RET

  !> Local variables
  TYPE(HOOKS_T) :: HOOKS
  TYPE(C_PTR), DIMENSION(:), POINTER :: C_DICT_P
  CHARACTER(LEN=32) :: F_DICT_TYPE
  INTEGER(KIND=C_SIZE_T) :: C_LEN_SZ

  !> Error Flags
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_DICTYPE_NOT_ASSOCIATED = 1_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_INVALID_LENGTH = 2_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_DEREFERENCE_DOUBLE_POINTER = 3_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_DICTIONARY_ALREADY_ASSOCIATED = 4_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UABLE_TO_COPY_CPTR_TO_F_STRING = 5_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION = 6_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UNKNOWN_DICTIONARY_TYPE = 666_JPIB_K


  ! Local variables declared by the preprocessor for debugging purposes
  PP_DEBUG_DECL_VARS

  ! Local variables declared by the preprocessor for logging purposes
  PP_LOG_DECL_VARS

  ! Local variables declared by the preprocessor for tracing purposes
  PP_TRACE_DECL_VARS

  ! Initialization of good path return value
  PP_SET_ERR_SUCCESS( RET )

  ! Initialization of the hooks
  CALL HOOKS%DEBUG_HOOK_%INIT( )

  !> Error handling
  PP_DEBUG_CRITICAL_COND_THROW( .NOT.C_ASSOCIATED(C_DICT_TYPE_P), ERRFLAG_DICTYPE_NOT_ASSOCIATED )
  PP_DEBUG_CRITICAL_COND_THROW( C_LEN_I .LE. 0,  ERRFLAG_INVALID_LENGTH )
  PP_DEBUG_CRITICAL_COND_THROW( C_LEN_I .GT. 32, ERRFLAG_INVALID_LENGTH )

 !> Get the encoder pointer
  C_DICT_P => NULL()
  PP_TRYCALL(ERRFLAG_DEREFERENCE_DOUBLE_POINTER) DEREFERENCE_DOUBLE_C_POINTER( &
&     C_DICT_PP, 1_JPIB_K, C_DICT_P, HOOKS )

  !> Error handling
  PP_DEBUG_CRITICAL_COND_THROW( C_ASSOCIATED(C_DICT_P(1)), ERRFLAG_DICTIONARY_ALREADY_ASSOCIATED )

  !> Get the size of the dictionary type
  C_LEN_SZ = INT( C_LEN_I, KIND=C_SIZE_T )
  F_DICT_TYPE = REPEAT( ' ', 32 )
  PP_TRYCALL(ERRFLAG_UABLE_TO_COPY_CPTR_TO_F_STRING) COPY_CPTR_TO_F_STRING( C_DICT_TYPE_P, C_LEN_SZ, F_DICT_TYPE, HOOKS, TOLOWER=.TRUE. )

  !> Dictionary creation logic
  SELECT CASE ( TRIM(ADJUSTL(F_DICT_TYPE)) )

    CASE ( 'mars', 'm', 'mars-dict', 'mars_dict' )

!$omp critical(API_CREATE_DICTIONARY)

      ! Create the wrappend mars dictionary
      C_DICT_P(1) = C_NULL_PTR
      PP_TRYCALL(ERRFLAG_UNKNOWN_DICTIONARY_TYPE) MAKE_MARS_DICTIONARY( C_DICT_P(1), HOOKS )
      PP_DEBUG_CRITICAL_COND_THROW( .NOT.C_ASSOCIATED(C_DICT_P(1)), ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION )

!$omp end critical(API_CREATE_DICTIONARY)

  CASE ( 'parametrization', 'p', 'par', 'param', 'parametrization-dict', 'parametrization_dict', 'par-dict', 'par_dict', 'param-dict', 'param_dict' )

!$omp critical(API_CREATE_DICTIONARY)

      ! Create the wrappend parametrization dictionary
      C_DICT_P(1) = C_NULL_PTR
      PP_TRYCALL(ERRFLAG_UNKNOWN_DICTIONARY_TYPE) MAKE_PAR_DICTIONARY( C_DICT_P(1), HOOKS )
      PP_DEBUG_CRITICAL_COND_THROW( .NOT.C_ASSOCIATED(C_DICT_P(1)), ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION )

!$omp end critical(API_CREATE_DICTIONARY)

  CASE ( 'healpix', 'healpix_ring', 'healpix_nested' )

!$omp critical(API_CREATE_DICTIONARY)

      ! Create the wrappend healpix dictionary
      C_DICT_P(1) = C_NULL_PTR
      PP_TRYCALL(ERRFLAG_UNKNOWN_DICTIONARY_TYPE) MAKE_HEALPIX_DICTIONARY( C_DICT_P(1), HOOKS )
      PP_DEBUG_CRITICAL_COND_THROW( .NOT.C_ASSOCIATED(C_DICT_P(1)), ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION )

!$omp end critical(API_CREATE_DICTIONARY)


  CASE ( 'reducedgg', 'reduced-gg', 'geometryreducedgg', 'geometry-reduced-gg' )

!$omp critical(API_CREATE_DICTIONARY)

      ! Create the wrappend healpix dictionary
      C_DICT_P(1) = C_NULL_PTR
      PP_TRYCALL(ERRFLAG_UNKNOWN_DICTIONARY_TYPE) MAKE_REDUCED_GG_DICTIONARY( C_DICT_P(1), HOOKS )
      PP_DEBUG_CRITICAL_COND_THROW( .NOT.C_ASSOCIATED(C_DICT_P(1)), ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION )

!$omp end critical(API_CREATE_DICTIONARY)

  CASE ( 'regulargg', 'regular-gg', 'geometryregulargg', 'geometry-regular-gg' )

!$omp critical(API_CREATE_DICTIONARY)

      ! Create the wrappend healpix dictionary
      C_DICT_P(1) = C_NULL_PTR
      PP_TRYCALL(ERRFLAG_UNKNOWN_DICTIONARY_TYPE) MAKE_REGULAR_GG_DICTIONARY( C_DICT_P(1), HOOKS )
      PP_DEBUG_CRITICAL_COND_THROW( .NOT.C_ASSOCIATED(C_DICT_P(1)), ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION )

!$omp end critical(API_CREATE_DICTIONARY)

  CASE ( 'regularll', 'regular-ll', 'geometryregularll', 'geometry-regular-ll' )

!$omp critical(API_CREATE_DICTIONARY)

      ! Create the wrappend healpix dictionary
      C_DICT_P(1) = C_NULL_PTR
      PP_TRYCALL(ERRFLAG_UNKNOWN_DICTIONARY_TYPE) MAKE_REGULAR_LL_DICTIONARY( C_DICT_P(1), HOOKS )
      PP_DEBUG_CRITICAL_COND_THROW( .NOT.C_ASSOCIATED(C_DICT_P(1)), ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION )

!$omp end critical(API_CREATE_DICTIONARY)

  CASE ( 'sh', 'spherical-harmonics', 'sphericalharmonics', 'geometry-sh', 'geometry-spherical-harmonics', 'geometrysh', 'geometrysphericalharmonics' )

!$omp critical(API_CREATE_DICTIONARY)

      ! Create the wrappend healpix dictionary
      C_DICT_P(1) = C_NULL_PTR
      PP_TRYCALL(ERRFLAG_UNKNOWN_DICTIONARY_TYPE) MAKE_SH_DICTIONARY( C_DICT_P(1), HOOKS )
      PP_DEBUG_CRITICAL_COND_THROW( .NOT.C_ASSOCIATED(C_DICT_P(1)), ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION )

!$omp end critical(API_CREATE_DICTIONARY)

  CASE ('options', 'opt', 'cfg', 'configuration' )

!$omp critical(API_CREATE_DICTIONARY)

      ! Create the wrappend healpix dictionary
      C_DICT_P(1) = C_NULL_PTR
      PP_TRYCALL(ERRFLAG_UNKNOWN_DICTIONARY_TYPE) MAKE_OPT_DICTIONARY( C_DICT_P(1), HOOKS )
      PP_DEBUG_CRITICAL_COND_THROW( .NOT.C_ASSOCIATED(C_DICT_P(1)), ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION )

!$omp end critical(API_CREATE_DICTIONARY)

  CASE DEFAULT

    ! Unknown dictionary type
    PP_DEBUG_CRITICAL_THROW( ERRFLAG_UNKNOWN_DICTIONARY_TYPE )

  END SELECT

  !> Be sure we don't have any memory leaks
  CALL HOOKS%DEBUG_HOOK_%FREE( )

  ! Exit point (On success)
  RETURN

! Error handler
PP_ERROR_HANDLER

  ! Initialization of bad path return value
  PP_SET_ERR_FAILURE( RET )

#if defined( PP_DEBUG_ENABLE_ERROR_HANDLING )
!$omp critical(ERROR_HANDLER)

  BLOCK

    ! Error handling variables
    PP_DEBUG_PUSH_FRAME()

    SELECT CASE(ERRIDX)
    CASE (ERRFLAG_DICTYPE_NOT_ASSOCIATED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'The dictionary type pointer is not associated' )
    CASE (ERRFLAG_INVALID_LENGTH)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'The length of the dictionary type is invalid' )
    CASE (ERRFLAG_DEREFERENCE_DOUBLE_POINTER)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to dereference the double c pointer for the dictionary' )
    CASE (ERRFLAG_DICTIONARY_ALREADY_ASSOCIATED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'The dictionary pointer is already associated' )
    CASE (ERRFLAG_UABLE_TO_COPY_CPTR_TO_F_STRING)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to copy the c pointer to fortran string for the dictionary type' )
    CASE (ERRFLAG_UNKNOWN_DICTIONARY_TYPE)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unknown dictionary type: "'//TRIM(ADJUSTL(F_DICT_TYPE))//'"' )
    CASE (ERRFLAG_DICTIONARY_NOT_ASSOCIATED_AFTER_CREATION)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'The dictionary pointer is not associated after creation' )
    CASE DEFAULT
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unknown error' )
    END SELECT

    ! Print the error stack
    ! NOTE: This is important when c is calling this function. Is opens the error_unit
    WRITE(ERROR_UNIT,*) ' PRINT ERROR STACK FROM: "'//__FILE__//'":', __LINE__
    CALL HOOKS%DEBUG_HOOK_%PRINT_ERROR_STACK( ERROR_UNIT )

    ! Free the error stack
    CALL HOOKS%DEBUG_HOOK_%FREE( )

    ! Write the error message and stop the program
    PP_DEBUG_ABORT

  END BLOCK

!$omp end critical(ERROR_HANDLER)
#endif

  RETURN

END FUNCTION MULTIO_GRIB2_DICT_CREATE
