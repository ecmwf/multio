#define PP_PROCEDURE_TYPE 'FUNCTION'
#define PP_PROCEDURE_NAME 'MARS_DICTIONARY_WRAPPER_GET_VALUE_AS_STRING'
PP_THREAD_SAFE FUNCTION MARS_DICTIONARY_WRAPPER_GET_VALUE_AS_STRING( WRAPPED_MARS_DICTIONARY, KEY, VALUE, HOOKS ) RESULT(RET)

  !> Symbols imported from intrinsic modules.
  USE, INTRINSIC :: ISO_C_BINDING, ONLY: C_PTR
  USE, INTRINSIC :: ISO_C_BINDING, ONLY: C_CHAR

  ! Symbols imported from other modules within the project.
  USE :: DATAKINDS_DEF_MOD,             ONLY: JPIB_K
  USE :: HOOKS_MOD,                     ONLY: HOOKS_T
  USE :: FORTRAN_MESSAGE_MOD,           ONLY: FORTRAN_MESSAGE_T
  USE :: API_MARS_DICTIONARY_UTILS_MOD, ONLY: MARS_DICTIONARY_NAME2ITERATOR
  USE :: API_MARS_DICTIONARY_UTILS_MOD, ONLY: MARS_DICTIONARY_RANK
  USE :: API_MARS_DICTIONARY_UTILS_MOD, ONLY: MARS_DICTIONARY_SIZE
  USE :: API_MARS_DICTIONARY_UTILS_MOD, ONLY: MARS_DICTIONARY_GET_VALUE_AS_STRING
  USE :: API_MARS_DICTIONARY_UTILS_MOD, ONLY: MARS_DICTIONARY_GET_VALUE_AS_STRING_BY_ID
  USE :: API_GENERAL_UTILS_MOD,         ONLY: COPY_FSTRING_TO_CCHAR_ARRAY

  ! Symbols imported by the preprocessor for debugging purposes
  PP_DEBUG_USE_VARS

  ! Symbols imported by the preprocessor for logging purposes
  PP_LOG_USE_VARS

  ! Symbols imported by the preprocessor for tracing purposes
  PP_TRACE_USE_VARS

IMPLICIT NONE

  !> Dummy arguments
  TYPE(C_PTR),                                INTENT(IN)     :: WRAPPED_MARS_DICTIONARY
  CHARACTER(LEN=*),                           INTENT(IN)     :: KEY
  CHARACTER(LEN=1,KIND=C_CHAR), DIMENSION(:), INTENT(OUT)    :: VALUE
  TYPE(HOOKS_T),                              INTENT(INOUT)  :: HOOKS

  !> Function result
  INTEGER(KIND=JPIB_K) :: RET

  ! Local variables
  INTEGER(KIND=JPIB_K) :: ITERATOR
  INTEGER(KIND=JPIB_K) :: LEN
  INTEGER(KIND=JPIB_K) :: NEEDED_LEN
  INTEGER(KIND=JPIB_K) :: RANK
  INTEGER(KIND=JPIB_K) :: SZ
  INTEGER(KIND=JPIB_K) :: ID
  INTEGER(KIND=JPIB_K) :: LO
  INTEGER(KIND=JPIB_K) :: HI
  INTEGER(KIND=JPIB_K) :: CURSOR
  TYPE(FORTRAN_MESSAGE_T), POINTER :: DICTIONARY
  CHARACTER(LEN=1024) :: VALUE_TMP
  LOGICAL :: HAS

  ! Local error flags
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_EXTRACT_DICTIONARY=1_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_MARS_DICTIONARY_NOT_ASSOCIATED=2_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_LEN_MUST_BE_BIGGER_THAN_ZERO=3_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_INVALID_ID=4_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_INVALID_RANK=5_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UNABLE_TO_GET_ITERATOR=6_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UNABLE_TO_GET_RANK=7_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UNABLE_TO_GET_SIZE=8_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UNABLE_TO_GET_VALUE_AS_STRING=9_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UNABLE_TO_GET_VALUE_AS_STRING_BY_ID=10_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_NOT_CONFIGURED=11_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_WRONG_LENGTH=12_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UNABLE_TO_COPY_TO_CCHAR_ARRAY=13_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_OUT_OF_BOUNDS=14_JPIB_K
  INTEGER(KIND=JPIB_K), PARAMETER :: ERRFLAG_UNABLE_TO_GET_STRING_SIZE=15_JPIB_K

  ! Local variables declared by the preprocessor for debugging purposes
  PP_DEBUG_DECL_VARS

  ! Local variables declared by the preprocessor for logging purposes
  PP_LOG_DECL_VARS

  ! Local variables declared by the preprocessor for tracing purposes
  PP_TRACE_DECL_VARS

  ! Trace begin of procedure
  PP_TRACE_ENTER_PROCEDURE()

  ! Initialization of good path return value
  PP_SET_ERR_SUCCESS( RET )

  ! Get the length of the value (should include the null terminator)
  LEN = SIZE(VALUE)

  ! Extract the encoder
  PP_TRYCALL(ERRFLAG_EXTRACT_DICTIONARY) MARS_DICTIONARY_WRAPPER_EXTRACT( WRAPPED_MARS_DICTIONARY, DICTIONARY, HOOKS )
  PP_DEBUG_CRITICAL_COND_THROW( .NOT.ASSOCIATED(DICTIONARY), ERRFLAG_MARS_DICTIONARY_NOT_ASSOCIATED )
  PP_DEBUG_CRITICAL_COND_THROW( LEN.LE.0_JPIB_K, ERRFLAG_LEN_MUST_BE_BIGGER_THAN_ZERO )

  ! Get enumerator from the dictionary
  PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_ITERATOR) MARS_DICTIONARY_NAME2ITERATOR( DICTIONARY, KEY, ITERATOR, HOOKS )

  ! Get the rank of the key in the dictionary
  PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_RANK) MARS_DICTIONARY_RANK( DICTIONARY, ITERATOR, RANK, HOOKS )

  ! Get the size of the key in the dictionary
  PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_SIZE) MARS_DICTIONARY_SIZE( DICTIONARY, ITERATOR, SZ, HOOKS )

  ! Pupulate hte output buffer
  HAS = .FALSE.
  VALUE_TMP = REPEAT(' ', 1024)
  IF ( RANK .EQ. 0_JPIB_K ) THEN

    ! Value is scalar, get the value as fortran string

    ! Get the value as fortran string
    PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_VALUE_AS_STRING) MARS_DICTIONARY_GET_VALUE_AS_STRING( DICTIONARY, ITERATOR, VALUE_TMP, HAS, HOOKS )
    PP_DEBUG_CRITICAL_COND_THROW( .NOT.HAS, ERRFLAG_NOT_CONFIGURED )

    ! Convert the fortran string to a c-pointer
    PP_DEBUG_CRITICAL_COND_THROW( (LEN_TRIM(ADJUSTL(VALUE_TMP))+1).NE.LEN, ERRFLAG_WRONG_LENGTH )
    PP_TRYCALL(ERRFLAG_UNABLE_TO_COPY_TO_CCHAR_ARRAY) COPY_FSTRING_TO_CCHAR_ARRAY(VALUE_TMP, VALUE, HOOKS, INITIALIZE=.TRUE.)

  ELSEIF ( RANK .EQ. 1_JPIB_K ) THEN

    ! Value is an array, get the value as fortran string in format '[value1, value2, ...]'

    ! Get the value as fortran string by id (only if the index is valid and the rank of the field is 1)
    PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_STRING_SIZE) MARS_DICTIONARY_WRAPPER_GET_STRING_SIZE( WRAPPED_MARS_DICTIONARY, KEY, NEEDED_LEN, HOOKS )

    ! Check string size
    PP_DEBUG_CRITICAL_COND_THROW( NEEDED_LEN.GT.LEN, ERRFLAG_WRONG_LENGTH )

    ! Loop over the elements of the array to populate the output buffer
    CURSOR = 1_JPIB_K
    VALUE(CURSOR) = '['
    CURSOR = CURSOR + 1_JPIB_K
    DO ID = 1_JPIB_K, SZ

      ! Get the value as fortran string
      PP_TRYCALL(ERRFLAG_UNABLE_TO_GET_VALUE_AS_STRING) MARS_DICTIONARY_GET_VALUE_AS_STRING_BY_ID( DICTIONARY, ITERATOR, ID, VALUE_TMP, HAS, HOOKS )
      PP_DEBUG_CRITICAL_COND_THROW( .NOT.HAS, ERRFLAG_NOT_CONFIGURED )

      ! Copy temporary values to output string
      LO = CURSOR + 1_JPIB_K
      HI = CURSOR + LEN_TRIM(ADJUSTL(VALUE_TMP))
      CURSOR = CURSOR + LEN_TRIM(ADJUSTL(VALUE_TMP))
      PP_DEBUG_CRITICAL_COND_THROW( CURSOR.GT.LEN, ERRFLAG_OUT_OF_BOUNDS )
      PP_TRYCALL(ERRFLAG_UNABLE_TO_COPY_TO_CCHAR_ARRAY) COPY_FSTRING_TO_CCHAR_ARRAY( VALUE_TMP, VALUE(LO:HI), HOOKS, INITIALIZE=.TRUE.)

      ! Inject separator
      IF ( ID .EQ. SZ ) THEN
        CURSOR = CURSOR + 1
        PP_DEBUG_CRITICAL_COND_THROW( CURSOR.GT.LEN, ERRFLAG_OUT_OF_BOUNDS )
        VALUE(CURSOR) = ']'
      ELSE
        CURSOR = CURSOR + 1
        PP_DEBUG_CRITICAL_COND_THROW( CURSOR.GT.LEN, ERRFLAG_OUT_OF_BOUNDS )
        VALUE(CURSOR) = ', '
        CURSOR = CURSOR + 1
        PP_DEBUG_CRITICAL_COND_THROW( CURSOR.GT.LEN, ERRFLAG_OUT_OF_BOUNDS )
        VALUE(CURSOR) = ' '
      END IF
    END DO

  ELSE

    PP_DEBUG_CRITICAL_THROW( ERRFLAG_INVALID_RANK )

  END IF

  ! Trace end of procedure (on success)
  PP_TRACE_EXIT_PROCEDURE_ON_SUCCESS()

  ! Exit point on success
  RETURN

! Error handler
PP_ERROR_HANDLER

  ! Initialization of bad path return value
  PP_SET_ERR_FAILURE( RET )

#if defined( PP_DEBUG_ENABLE_ERROR_HANDLING )
!$omp critical(ERROR_HANDLER)

  BLOCK

    CHARACTER(LEN=32) :: CTMP

    ! Error handling variables
    PP_DEBUG_PUSH_FRAME()

    ! HAndle different errors
    SELECT CASE(ERRIDX)
    CASE (ERRFLAG_EXTRACT_DICTIONARY)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to extract mars dictionary' )
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Key: ' // TRIM(ADJUSTL(KEY)) )
    CASE (ERRFLAG_MARS_DICTIONARY_NOT_ASSOCIATED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'MARS dictionary not associated' )
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Key: ' // TRIM(ADJUSTL(KEY)) )
    CASE (ERRFLAG_LEN_MUST_BE_BIGGER_THAN_ZERO)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Length must be bigger than zero' )
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Key: ' // TRIM(ADJUSTL(KEY)) )
      CTMP = REPEAT(' ', 32)
      WRITE(CTMP, '(I32)') LEN
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Length: ' // TRIM(ADJUSTL(CTMP)) )
    CASE (ERRFLAG_INVALID_ID)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'ID must be bigger than 0' )
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Key: ' // TRIM(ADJUSTL(KEY)) )
      CTMP = REPEAT(' ', 32)
      WRITE(CTMP, '(I32)') ID
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'ID: ' // TRIM(ADJUSTL(CTMP)) )
    CASE (ERRFLAG_UNABLE_TO_GET_ITERATOR)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Unable to get iterator' )
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'Key: ' // TRIM(ADJUSTL(KEY)) )
