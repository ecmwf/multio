    PP_DEBUG_CRITICAL_COND_THROW( ALLOC_STATUS.NE.0, ERRFLAG_UNABLE_TO_ALLOCATE )
  CLASS IS (MULTIO_METADATA_T)
    ALLOCATE( MULTIO_METADATA_T::OUT_METADATA, STAT=ALLOC_STATUS, ERRMSG=ERRMSG )
    PP_DEBUG_CRITICAL_COND_THROW( ALLOC_STATUS.NE.0, ERRFLAG_UNABLE_TO_ALLOCATE )
    SELECT TYPE(B => OUT_METADATA)
    CLASS IS (MULTIO_METADATA_T)
      PP_TRYCALL(ERRFLAG_UNABLE_GET_MIOH) A%GET_MULTIO_HANDLE( MIOH, HOOKS )
      PP_TRYCALL(ERRFLAG_UNABLE_SET_MIOH) B%SET_MULTIO_HANDLE( MIOH, HOOKS )
    CLASS DEFAULT
      PP_DEBUG_CRITICAL_THROW( ERRFLAG_UNKNOWN_TYPE )
    ENDSELECT
  CLASS IS (LOG_METADATA_T)
    ALLOCATE( LOG_METADATA_T::OUT_METADATA, STAT=ALLOC_STATUS, ERRMSG=ERRMSG )
    PP_DEBUG_CRITICAL_COND_THROW( ALLOC_STATUS.NE.0, ERRFLAG_UNABLE_TO_ALLOCATE )
  CLASS DEFAULT
    PP_DEBUG_CRITICAL_THROW( ERRFLAG_UNKNOWN_TYPE )
  END SELECT

  ! Erro handling
  PP_DEBUG_CRITICAL_COND_THROW( .NOT.ASSOCIATED(OUT_METADATA), ERRFLAG_OUTPUT_METADATA_NOT_ASSOCIATED )

  ! Copy the metadata (Need this instead of allocating with source because the source
  ! need to be cloned/duplicated, not just copied - especially for grib handles)
  PP_TRYCALL(ERRFLAG_UNABLE_TO_INITIALIZE) OUT_METADATA%INIT_FROM_SAMPLE_NAME( SAMPLE_NAME, HOOKS )

  ! Check initialization
  PP_TRYCALL(ERRFLAG_UNABLE_CHECK_INITIALIZATION) OUT_METADATA%INITIALIZED( INITIALIZED, HOOKS )
  PP_DEBUG_CRITICAL_COND_THROW( .NOT.INITIALIZED, ERRFLAG_OUTPUT_METADATA_NOT_INITIALIZED )

  ! Trace end of procedure (on success)
  PP_TRACE_EXIT_PROCEDURE_ON_SUCCESS()

  ! Exit point (On success)
  RETURN

! Error handler
PP_ERROR_HANDLER

  ! Initialization of bad path return value
  PP_SET_ERR_FAILURE( RET )

#if defined( PP_DEBUG_ENABLE_ERROR_HANDLING )
!$omp critical(ERROR_HANDLER)

  BLOCK

    ! Error handling variables
    PP_DEBUG_PUSH_FRAME()

    ! Handle different errors
    SELECT CASE(ERRIDX)
    CASE (ERRFLAG_UNKNOWN_TYPE)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'unknown metadata type' )
    CASE (ERRFLAG_UNABLE_TO_ALLOCATE)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'unable to allocate memory for metadata' )
      IF ( ALLOCATED(ERRMSG) ) THEN
        PP_DEBUG_PUSH_MSG_TO_FRAME( 'error message: ' // TRIM(ERRMSG) )
      ENDIF
    CASE (ERRFLAG_UNABLE_TO_INITIALIZE)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'unable to initialize metadata' )
    CASE (ERRFLAG_INPUT_METADATA_NOT_ASSOCIATED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'input metadata not associated' )
    CASE (ERRFLAG_OUTPUT_METADATA_ALREADY_ASSOCIATED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'output metadata already associated' )
    CASE (ERRFLAG_OUTPUT_METADATA_NOT_ASSOCIATED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'output metadata not associated' )
    CASE (ERRFLAG_OUTPUT_METADATA_NOT_INITIALIZED)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'output metadata not initialized' )
    CASE (ERRFLAG_UNABLE_CHECK_INITIALIZATION)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'unable to check initialization' )
    CASE (ERRFLAG_UNABLE_GET_MIOH)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'unable to get multio handle' )
    CASE (ERRFLAG_UNABLE_SET_MIOH)
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'unable to set multio handle' )
    CASE DEFAULT
      PP_DEBUG_PUSH_MSG_TO_FRAME( 'unhandled error' )
    END SELECT

    ! Trace end of procedure (on error)
    PP_TRACE_EXIT_PROCEDURE_ON_ERROR()

    ! Write the error message and stop the program
    PP_DEBUG_ABORT

  END BLOCK

!$omp end critical(ERROR_HANDLER)
#endif

  ! Exit point (on error)
  RETURN


END FUNCTION CLONE_METADATA_AND_LOAD_SAMPLE_NAME
